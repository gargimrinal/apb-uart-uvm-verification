interface uart_if(input logic clk);

    //--------------------------------------------------
    // Reset
    //--------------------------------------------------
    logic rst_n;

    //--------------------------------------------------
    // UART Serial Pins
    //--------------------------------------------------
    logic tx;
    logic rx;

    //--------------------------------------------------
    // Interrupt/Event
    //--------------------------------------------------
    logic event_o;

    //--------------------------------------------------
    // UART Configuration (from DUT)
    //--------------------------------------------------
    logic [15:0] baud_div;
    logic [1:0]  data_bits;
    logic        parity_enable;
    logic        stop_bits;

    //--------------------------------------------------
    // UART Status (from DUT)
    //--------------------------------------------------
    logic        rx_valid;
    logic        parity_error;

endinterface